print('Aula #12 - Operadores de atribuição. \n')

# Um sinal de '=' em linguagem de programação 'Python' é para atribuir
# valores a variáveis.

resultado = 2
print('O valor de resultado: ', resultado)

# Alterando o valor da variável resultado
resultado = 3
print('O valor de resultado: ', resultado)

# As variáveis em 'Python" não tem tipo, pode-se alterar o valor da variável para qq tipo.
resultado = 'Caracteres'
print('O valor de resultado: ', resultado)

# Adição aditiva
resultado = 2
print('O valor de resultado: ', resultado)
resultado += 3
print('O valor de resultado: ', resultado)

# Adição subtrativa
resultado -= 4
print('O valor de resultado: ', resultado)

# Adição multiplicativa
resultado *= 16
print('O valor de resultado: ', resultado)

# Adição divisão
resultado /= 3
print('O valor de resultado: ', resultado)

# Adição modular
resultado %= 5
print('O valor de resultado: ', resultado)