
print('Aula #11 - Operadores relacionais. \n')

#Declarando variáveis para exercícios.
x = 7
y = 5
print('x = ', x)
print('y = ', y)

# Aplicando operadores relacionais - retorno sera booleano (true ou false)
print('x maior que y: ', x > y)
print( 'x maior igual que y: ', x >= y)
print('x menor que y: ',  x < y)
print('x menor igual que y: ',  x <= y)
print( 'x é diferente de y: ', x != y)
print( 'x é igual de y: ', x == y)